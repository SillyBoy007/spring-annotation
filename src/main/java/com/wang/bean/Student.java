package com.wang.bean;


import org.springframework.beans.factory.annotation.Value;

public class Student {
    @Value("${student.id}")
    private String id;
    @Value("${student.name}")
    private String name;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Student{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
